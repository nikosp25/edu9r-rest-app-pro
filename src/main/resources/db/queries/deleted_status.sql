--Το query αυτό εμφανίζει όλους τους teachers (ενεργούς και διεγραμμένους μαζί) με τα στοιχεία τους.
--Για κάθε teacher εμφανίζει:

--ΠΕΡΙΟΧΗ — η περιοχή του teacher
--ΌΝΟΜΑ / ΕΠΩΝΥΜΟ — το ονοματεπώνυμό του
--ΑΜΚΑ / ΑΦΜ — τα προσωπικά του στοιχεία
--ΚΑΤΑΣΤΑΣΗ — αν είναι ΕΝΕΡΓΟΣ ή ΔΙΕΓΡΑΜΜΕΝΟΣ
--ΕΜΠΕΙΡΙΑ — κατηγοριοποίηση βάσει πότε δημιουργήθηκε ο λογαριασμός του (ΝΕΟΣ, ΜΕΣΑΙΟΣ, ΕΜΠΕΙΡΟΣ, ΠΑΛΙΟΣ)

--Παράδειγμα αποτελέσματος:
--ΠΕΡΙΟΧΗ       ΌΝΟΜΑ   ΕΠΩΝΥΜΟ         ΑΜΚΑ        ΑΦΜ         ΚΑΤΑΣΤΑΣΗ       ΕΜΠΕΙΡΙΑ
--Αθήνα         Γιώργος Παπαδόπουλος    12345678901 123456789   ΔΙΕΓΡΑΜΜΕΝΟΣ    ΠΑΛΙΟΣ
--Αθήνα         Μαρία   Κωνσταντίνου    98765432101 987654321   ΔΙΕΓΡΑΜΜΕΝΟΣ    ΝΕΟΣ
--Θεσσαλονίκη   Νίκος   Αλεξίου         11223344556 112233445   ΕΝΕΡΓΟΣ         ΜΕΣΑΙΟΣ

--Η σειρά εμφάνισης (ORDER BY t.deleted DESC, r.name):

--Πρώτα εμφανίζονται οι ΔΙΕΓΡΑΜΜΕΝΟΙ (deleted=1 πρώτο λόγω DESC)
--Και μέσα σε κάθε ομάδα ταξινομούνται αλφαβητικά ανά περιοχή

SELECT
    r.name AS 'ΠΕΡΙΟΧΗ',
    t.firstname AS 'ΌΝΟΜΑ',
    t.lastname AS 'ΕΠΩΝΥΜΟ',
    pi.amka AS 'ΑΜΚΑ',
    t.vat AS 'ΑΦΜ',
    CASE
        WHEN t.deleted = 1 THEN 'ΔΙΕΓΡΑΜΜΕΝΟΣ'
        ELSE 'ΕΝΕΡΓΟΣ'
    END AS 'ΚΑΤΑΣΤΑΣΗ',
    CASE
        WHEN t.created_at > '2025-01-01' THEN 'ΝΕΟΣ'
        WHEN t.created_at > '2023-01-01' THEN 'ΜΕΣΑΙΟΣ'
        WHEN t.created_at > '2020-01-01' THEN 'ΕΜΠΕΙΡΟΣ'
        ELSE 'ΠΑΛΙΟΣ'
    END AS 'ΕΜΠΕΙΡΙΑ'
FROM teachers t
JOIN personal_information pi ON t.personal_info_id = pi.id
JOIN regions r ON t.region_id = r.id
WHERE t.deleted = 0	-- delete this filter for getting both
ORDER BY t.deleted DESC, r.name;